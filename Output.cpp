#include <iostream>
using namespace std;

int main() {
    // Text Output
    cout << "Hello World!";
    cout << " This will be in the buffer and then printed" << endl;
    cout << "This is a second statement \n";
    cout << "#include <iostream> is a header library";
    cout << "\n";
    
    // Number Output
    cout << 5 << endl;
    cout << 5+5 << endl;

    // Escape Sequences
    cout << "This is a new line \n";
    cout << "This is a tab \t" << "Hello \n";
    cout << "This is a backslash \\" << endl;
    cout << "This is a double quote \"" << endl;

    return 0;
}